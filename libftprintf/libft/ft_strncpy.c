#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t	i;

	i = 0;
	while (src[i] && i < len)
	{
		dst[i] = (unsigned char)src[i];
		i++;
	}
	while (i < len)
		dst[i++] = (unsigned char)'\0';
	return (dst);
}
